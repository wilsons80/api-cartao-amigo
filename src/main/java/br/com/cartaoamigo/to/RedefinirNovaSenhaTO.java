public class RedefinirNovaSenhaTO {
	
	private Long idRedefinirSenha;
	private String senha1;
	private String senha2;
	
	public RedefinirNovaSenhaTO() {
	}

	public Long getIdRedefinirSenha() {
		return idRedefinirSenha;
	}

	public void setIdRedefinirSenha(Long idRedefinirSenha) {
		this.idRedefinirSenha = idRedefinirSenha;
	}

	public String getSenha1() {
		return senha1;
	}

	public void setSenha1(String senha1) {
		this.senha1 = senha1;
	}

	public String getSenha2() {
		return senha2;
	}

	public void setSenha2(String senha2) {
		this.senha2 = senha2;
	}
		

}
